/* ── Helper buat SVG dengan ukuran custom ── */
function buatSVG(path, w, h, extra) {
  w = w || 16;
  h = h || 16;
  extra = extra || '';
  return '<svg xmlns="http://www.w3.org/2000/svg" width="' + w + '" height="' + h + '" ' +
    'viewBox="0 0 24 24" fill="none" stroke="currentColor" ' +
    'stroke-width="2" stroke-linecap="round" stroke-linejoin="round" ' + extra + '>' +
    path + '</svg>';
}

/* ── Fungsi publik: icon dengan ukuran custom ── */
function iconSvg(nama, w, h) {
  var paths = IconPaths;
  if (!paths[nama]) return '';
  return buatSVG(paths[nama], w || 16, h || 16);
}

